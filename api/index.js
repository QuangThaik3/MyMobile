const path = require('path');
const sharp = require('sharp');

dotenv.config();

mongoose.connect(process.env.MONGO_URL)
.then(() => console.log("DB Connection Successfull!"))
.catch((err) => {
    console.log(err);
});

const storage = multer.diskStorage({
    destination: function(req, file, cb) {
        cb(null, path.join(__dirname, "../app/public/upload")); 
    },
    filename: function (req, file, cb) {
        cb(null, Date.now() + file.originalname);
    }
});


const upload = multer({ storage: storage });

app.post('/api/upload/:id', upload.single('file'), async (req, res) => {
    try {
        const file = req.file;
        const userId = req.params.id;
    
        const filePath = file.path;

        const buffer = await sharp(filePath).resize({ width: 300, height: 300 }).toBuffer();
        const base64Image = buffer.toString('base64');

        const updatedUser = await User.findByIdAndUpdate(userId, { img: base64Image }, { new: true });
    
        return res.json({ success: true, user: updatedUser });
      } catch (err) {
        return res.status(500).json({ error: err.message });
      }
  });



app.use(cors());
app.use(express.json());
app.use("/api/products", productRoute);
app.use("/api/auth", authRoute);
app.use("/api/orders", orderRoute);

